# 정렬·표시 우선순위: 구매형 → 중간 → 정보형.
INTENT_RANK = {"buy": 0, "mid": 1, "info": 2}


def _normalize(text: str) -> str:
    """소문자화 + 모든 공백 제거 — 대소문자·띄어쓰기 무관 매칭용."""
    return re.sub(r"\s+", "", (text or "")).lower()


_INFO = {_normalize(w) for w in _INFO_RAW}
_BUY = {_normalize(w) for w in _BUY_RAW}
_MID = {_normalize(w) for w in _MID_RAW}


def classify_intent(keyword: str) -> str:
    """키워드를 'buy' | 'mid' | 'info' 로 분류한다(위 우선순위 규칙).

    빈 키워드·미분류는 'mid'. 정보형 단어가 있으면 무조건 'info'.
    그 외에는 구매형·중간 중 가장 긴 매칭 단어를 따르고, 길이 동률이면 구매형 우선.
    """
    k = _normalize(keyword)
    if not k:
        return "mid"

    # 1) 정보형 절대 우선 — 사용법·위치 등 단어가 하나라도 포함되면 🔴.
    if any(w in k for w in _INFO):
        return "info"

    # 2) 구매형·중간 중 ★가장 긴 매칭. 구매형을 먼저 보고 '>' 비교로 동률 시 구매형 유지.
    best_len = 0
    best_cat = None
    for w in _BUY:
        if len(w) > best_len and w in k:
            best_len, best_cat = len(w), "buy"
    for w in _MID:
        if len(w) > best_len and w in k:   # 더 길 때만 중간이 구매형을 이긴다
            best_len, best_cat = len(w), "mid"

    # 3) 아무것도 안 걸리면 기본 중간.
    return best_cat or "mid"


def annotate_intent(pairs: list[tuple]) -> list[tuple]:
    """[(kw, val), ...] → [(kw, val, intent), ...]. 원래 순서 보존."""
    return [(kw, val, classify_intent(kw)) for kw, val in pairs]


def sort_by_intent(pairs: list[tuple]) -> list[tuple]:
    """[(kw, val), ...] 를 구매형→중간→정보형 순으로 ★안정 정렬(그룹 내 원래 순서 유지)."""
    return sorted(pairs, key=lambda t: INTENT_RANK[classify_intent(t[0])])
